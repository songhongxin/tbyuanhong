package com.etao.lz.star.utils;

public class ShopInfo {
	
	private String _nick;
	private String _shop_id;
	private String _shop_type;
	private long _user_id;
	private long _unit_id;
	
	public ShopInfo() {
		_nick = _shop_id = _shop_type = "";
		_user_id = 0;
		_unit_id = 0;
	}
	
	public String getNick () {
		return _nick;
	}
	public String getShopId() {
		return _shop_id;
	}
	public String getShopType() {
		return _shop_type;
	}
	public long getUserId() {
		return _user_id;
	}
	public long getUnitId() {
		return _unit_id;
	}
	public void setNick (String nick) {
		_nick = nick;
	}
	public void setShopId(String shop_id) {
		_shop_id = shop_id;
	}
	public void setShopType(String shop_type) {
		_shop_type = shop_type;
	}
	public void setUserId(long user_id) {
		_user_id = user_id;
	}
	public void setUnitId(long unit_id) {
		_unit_id = unit_id;
	}

}